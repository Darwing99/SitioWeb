                      <p class="fondo_historia fuente  blockquote text-justify text-left pt-1">
                        A la sombra del legendario árbol de liquidámbar, nace y crece nuestro café, en la majestuosa montaña de Cantiles, ubicada en la Comunidad de Monte Vista, en el Municipio de San José De Colinas, Santa Bárbara, Honduras a 1440 metros de altura.
                      <br>
                        Entre el cantar de las aves, el sonido intimidante de los monos aulladores, y la mirada sigilosa del lémur y entre los juegos despreocupados de las simpáticas ardillas.
                      <br>
                        Así nace una de las historias más aromáticas, en medio del sentir de la suave brisa y el aire fresco que nos regala nuestro bosque húmedo y con el sonido relajante de las fuentes de agua que la rodean, pero sobre todo con las ocurrencias de Don Alex.
                      <br>
                        Así es Finca La Ruta, un lugar con mucha historia, que se remonta a enero de 1972, cuando la mula cansada que Transporto a nuestro Abuelo El Dr. Juan Paz Rivera intentaba sacar sus patas traseras del rojizo y encharcado barro que cubría la pendiente del único camino transitable entre aquellas escarpadas montañas.
                      <br>
                        El y dos hombres que lo acompañaban en caravana dirigida por a don Antonio Paredes, fiel capataz de la familia, quien servía por 5 años en las verdes llanuras a la par del rio pastoreando y cuidando el ganado, como segundo y por último don Teófilo Enamorado, segundo capataz, bajo las órdenes del Compadre Antonio.
                      <br>
                        Ahí por la aldea La Alianza, después de casi un día de viaje, entre la densa niebla y la fina llovizna, en donde apenas se podía observar aquel trio de hombres con sus espaldas cubiertas de plásticos para escapar un poco de la lluvia y del frio que los hacía temblar.
                    <br>
                        En aquellos momentos nuestro abuelo el Dr. Rivera se preguntaba si había sido buena idea el viaje y más aún el plan de comprar una propiedad en el corazón de la montaña, en su mente aun resonaban las palabras de su esposa Ofelia “Que vas a hacer en ese monte” ni se puede llegar allí y vos queriendo comprar mejor dedícate a trabajar en la medicina”
                        por fin pudieron llegar al punto más alto del pueblo de Colinas, la famosa y magnifica montaña de CANTILES, siempre cubierta de nubes blancas y una espesa niebla, que apenas dejaba escapar las puntas de los imponentes pinos, liquidámbar y caretos; Cantiles, enigmática y temida, guardiana de secretos y origen de historias, hogar salvaje de tigrillos, zorros y lémures, lirico ecosistema burbujeante de vida.
                    <br>
                        A su encuentro, salió lentamente don Anastasio Rápalo un viejo de 73 años cuyas arrugas invadían su rostro, esculpiendo una sonrisa que parecía petrificada, y al viento su mirada penetrante de unos ojos azules que se confundía con el cielo de verano, el viejo era viudo su mujer Doña Concepción había fallecido tres años atrás, dejándolo solo y sin hijos que trabajaran la tierra.
                    <br>
                        En ese momento Don Anastasio se disculpó, con nuestro abuelo por el estado de la propiedad que estaba vendiendo, ya que lucía algo descuidada y con pocas plantas de café 
                        pero sus palabras sabias y convincentes marcaron la acertada decisión de la compra 
                        “Mi apreciado Dr. Rivera no se engañe por las apariencias que sus ojos ven, no le quiero vender una finca, yo le estoy vendiendo una tierra única, fértil como ninguna, estamos en el punto más alto de Colinas, el clima aquí es increíble ,el café que esta tierra nos regala no tiene comparación”.
                    <br>
                        Ese día a esa hora, no fueron necesarias más palabras, en aquellos tiempos los acuerdos verbales y las promesas tenían valor, la decisión fue tomada, ahora el Dr. Juan Paz Rivera empezaba una aventura lejos de casa, llena de sueños y anhelos, estaba sin saberlo escribiendo en el ADN de sus generaciones la pasión y el amor por el CAFÉ.
                    <br>
                        Hablar de Juan Paz Rivera, es hablar de un gran personaje, Médico que dedico su profesión a su gente, y vivía de otros ingresos como la Agricultura y la Ganadería era conocido como el Ojo clínico por sus atinados diagnósticos, fantásticas historias que en la actualidad circulan entre los pobladores.
                    <br>
                        Su habilidad para los negocios permitió que la Finca, se mantuviera con producciones envidiables y muy rentables, esto acompañado de los precios excelentes que manejaba el café de Honduras en aquellos tiempos garantizaban un futuro prometedor, pero a principios de los ochentas fue diagnosticado con Cáncer, una enfermedad cruel que le arrebata la vida a principios de 1983 significando un duro golpe para la familia.
                      <br>
                        La propiedad quedo prácticamente abandonada, para el año 2005 nuestro padre enfermo quedando incapacitado para trabajar, a temprana edad, teníamos que trabajar para poder solventar sus gastos y cada quien en áreas diferentes aportaba al hogar.
                        En estos años la producción de café en Honduras baja de forma considerable debido a la poca rentabilidad, bajos precios en el mercado internacional y el surgimiento de nuevas plagas que elevaron aún más los costos de mantenimientos.
                      <br>
                        Por factores como estos los jóvenes en Honduras, optan por no seguir el negocio del café, debido al riesgo de inversión, cuando alguien quiere invertir son muchas las advertencias que lo ponen en dudas, las propiedades heredadas se cultivan productos sustitutos o con mejores pronósticos, las organizaciones gremiales, se han convertido en grupos cerrado que benefician a pocos, aportando poco o nada para las grandes mayorías.
                      <br>
                        Aun con todos estos factores en contra En el año 2015 decidimos emprender, y fundamos una pequeña cafetería en la ciudad de santa bárbara, aprovechando el buen momento de las cafeterías en otras ciudades y con el objetivo de difundir la cultura de café en nuestra ciudad, con muchos sueños e ideas, pero con la mente también en promocionar el café que se produce en nuestro departamento y ser un lugar donde convergieran deliciosos cafés de diversas zonas del mismo, así nace La Ruta Café.
                      <br>
                        El camino no fue nada fácil, no teníamos mucha experiencia, poco Dinero y muchos sueños y comenzamos a vivir varias tormentas, cambios de local, para poder aumentar los ingresos, innovaciones, experimentos, pero siempre nos costaba mucho trabajo mantener las operaciones. Con el tiempo los competidores hicieron su aparición y el pastel comenzó a dividirse, dificultando el camino aún más.
                        En tiempo de tormenta, sacas fuerza de donde sea necesario, para mantenerte vivo, para aferrarte a tu sueño, para tener un futuro mejor.
                      <br>
                        Comenzamos con la difícil tarea de innovar, bajo la teoría de la calidad supera la cantidad, para el año 2018 empezamos la aventura de producir realizando pequeñas inversiones en el levantamiento de la finca, con miras a convertimos años más adelante en una cafetería de especialidad y después de origen, logrando este sueño en el 2019 con la primera cosecha finca la ruta, quien adopta este nombre de la cafetería, quien hoy día se ha convertido en eso, en la ruta a seguir o desde donde convergen nuevos sueños y metas.
                      <br>
                        Hoy en día La Ruta Café no solo es una cafetería de origen, si no también es una cafetería con propósito, es un espacio para que jóvenes en riesgo social de nuestra ciudad, puedan encontrar en el barismo una oportunidad para cambiar su vida.
                      <br>
                        Honduras es un país difícil para emprender y más para ser joven, pero nosotros tratamos de invertir parte de nuestros ingresos en permitir que jóvenes se capaciten y realicen sus prácticas en nuestro espacio sin costo alguno, solventando algunos costos básicos para que puedan cumplir algún tiempo determinado que les permita tener un nivel aceptable, para aplicar a un trabajo en el rubro.
                      <br>
                        Nuestros baristas, provienen de la Escuela Taller Codemussba, una institución que capacita a jóvenes en diversas ramas sin costo alguno, con fondos de gobierno y algunas instituciones internacionales de cooperación con las que hemos formado una alianza de amistad.
                        También las puertas de la cafetería están abiertas para todo el que quiera aprender y descubrir sobre la cultura del café 
                      <br>
                        El 2018 fundamos el proyecto café cultura, en donde visitamos colegios públicos llevando horas de aprendizaje y mucho café a chicos que estudian bachilleratos en turismo compartiendo nuestros conocimientos con ellos, estas visitan son devueltas en grupos pequeños a nuestra cafetería para que los chicos vivan la experiencia día a día en operaciones.              
                      <br>
                        Al hablar de esta parte, me es imposible no emocionarme, y recordar todo lo que hemos logrado hasta hoy en día y todo el sacrificio y los malos momentos han valido la pena
                        Sin recibir, una capacitación o un curso formal, somos baristas, productores, procesamos café de especialidad, tostamos y de alguna forma controlamos todo el proceso, ofreciendo a cada cliente una taza de café muy especial, una llena de sueños. 
                    <br>
                        En el año 2020, la pandemia COVID-19 sorprendió al mundo, y nos cambió la vida a todos, pero con la Voluntad de Dios, que nunca nos ha abandonado, nos mantenemos de pie, y eso no ha sido excusa para dejar de soñar.
                        Hemos mantenido nuestras planillas, sin suspender a nuestros colaboradores, y los trabajos en la finca se han mantenido, en mi caso como encargado de cafería, camino 3 kilómetros a Diario ante la falta de transporte público, para poder apertura y poder generar ingresos que nos permitan mantenernos de pie, y manteniendo a las familias que dependen de nosotros.
                      </p>
